    /**
     * začiatok zobrazenia textového súboru
     * @param file - cesta k súboru na zobrazenie
     */
    public void loadTextFile(String file){

            isTViewed = true;
            
            FileInputStream FIS = null;
            try {
                FIS = new FileInputStream(file);
                DataInputStream DIS = new DataInputStream(FIS);
                BufferedReader reader = new BufferedReader(new InputStreamReader(DIS));
                
                StringBuilder str = new StringBuilder();
                String tempStr = reader.readLine();
                while(tempStr != null){
                    str.append(tempStr);
                    str.append(System.getProperty("line.separator"));
                    tempStr = reader.readLine();
                }
                
                jEditorPane1.setText(str.toString());
                this.setVisible(true);
                WasTextViewed = true;
                
                timer = new Timer();
                int time = (int) (SettingsLoader.getInstance().getDocumentsShowingLength() * 1000);
                timer.schedule(new RemindTask(this), time);
                
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(frame, "ERROR: Cannot load file. Please parse gps file again. ", "ERROR", JOptionPane.ERROR_MESSAGE);
            } finally {
                try {
                    if(FIS != null){
                        FIS.close();
                    }
                } catch (IOException ex) {

                }
            }                  
    }
    
    
