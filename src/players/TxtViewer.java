    /**
    * opätovné spustenie časovača dĺžky zobrazenia textového súboru
    */
    public void restartTimer() {
      timer = new Timer();
      int time = (int) (SettingsLoader.getInstance().getDocumentsShowingLength() * 1000);
      timer.schedule(new RemindTask(this), time);
  }

 static class RemindTask extends TimerTask {
     
     private JFrame frame;
     
     public RemindTask(JFrame frame){
         super();
         this.frame = frame;
     }

        public void run() {

            timer.cancel();

            if (Presentator.isClosingWindows == true) {
                    PictureViewer.frame.setVisible(false);
                    frame.setVisible(false);
                }

            isTViewed = false;

            Presentator.secondMultimediaSearchTimer.restart();

            if (Presentator.isClosingWindows == true) {
                    PictureViewer.frame.setVisible(false);
                }

        }
    }
    
    
